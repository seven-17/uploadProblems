package henu.acm.entity;

public class RankBean {
	
	private Integer user_id;
	private String user_name;
	private Integer aweekday;
	private Integer amonth;
	private Integer total;
	
	public Integer getUser_id() {
		return user_id;
	}
	public void setUser_id(Integer user_id) {
		this.user_id = user_id;
	}
	public String getUser_name() {
		return user_name;
	}
	public void setUser_name(String user_name) {
		this.user_name = user_name;
	}
	public Integer getAweekday() {
		return aweekday;
	}
	public void setAweekday(Integer aweekday) {
		this.aweekday = aweekday;
	}
	public Integer getAmonth() {
		return amonth;
	}
	public void setAmonth(Integer amonth) {
		this.amonth = amonth;
	}
	public Integer getTotal() {
		return total;
	}
	public void setTotal(Integer total) {
		this.total = total;
	}
	
}
